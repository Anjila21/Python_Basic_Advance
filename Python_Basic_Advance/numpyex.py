#  Numpy stands for numerical python
#  There are various types of data that needs to be converted into machine undestandable
# for this numpy converts the given data into array and matrices.

import numpy as np
#For 1 Dimensional array
_1darray = np.array([1,2,3,4])
print(_1darray)
print(type(_1darray))
print(_1darray.ndim)
print(_1darray.shape)

#For 2-D array
_2darray = np.array([[1,2,3,4,5],[7,8,9,10,11]])
print(_2darray)
print(type(_2darray))
print(_2darray.ndim)
print(_2darray.shape)




